import { readFileSync, writeFileSync, mkdirSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const rootDir = join(__dirname, "..");

// Read the manifest
const manifestPath = join(
  rootDir,
  "node_modules",
  "material-icon-theme",
  "dist",
  "material-icons.json",
);
const manifest = JSON.parse(readFileSync(manifestPath, "utf-8"));

// Extract mapping tables
const { fileExtensions, fileNames, folderNames, folderNamesExpanded, iconDefinitions } = manifest;

// Build the icon name mappings (lowercase keys for case-insensitive lookup)
const extMap = {};
for (const [ext, icon] of Object.entries(fileExtensions)) {
  extMap[ext.toLowerCase()] = icon;
}

const nameMap = {};
for (const [name, icon] of Object.entries(fileNames)) {
  nameMap[name.toLowerCase()] = icon;
}

const folderMap = {};
for (const [name, icon] of Object.entries(folderNames)) {
  folderMap[name.toLowerCase()] = icon;
}

const folderExpandedMap = {};
if (folderNamesExpanded) {
  for (const [name, icon] of Object.entries(folderNamesExpanded)) {
    folderExpandedMap[name.toLowerCase()] = icon;
  }
}

// Default icon names
const defaults = {
  file: manifest.file || "file",
  folder: manifest.folder || "folder",
  folderExpanded: manifest.folderExpanded || "folder-open",
  rootFolder: manifest.rootFolder || "folder",
  rootFolderExpanded: manifest.rootFolderExpanded || "folder-open",
};

// Generate TypeScript
const output = `// Auto-generated by scripts/generate-icon-manifest.mjs
// Do not edit manually — regenerate with: npm run generate-icons

export const fileExtensions: Record<string, string> = ${JSON.stringify(extMap, null, 2)};

export const fileNames: Record<string, string> = ${JSON.stringify(nameMap, null, 2)};

export const folderNames: Record<string, string> = ${JSON.stringify(folderMap, null, 2)};

export const folderNamesExpanded: Record<string, string> = ${JSON.stringify(folderExpandedMap, null, 2)};

export const defaults = ${JSON.stringify(defaults, null, 2)} as const;
`;

const outDir = join(rootDir, "src", "icons", "generated");
mkdirSync(outDir, { recursive: true });
writeFileSync(join(outDir, "iconManifest.ts"), output, "utf-8");

console.log(
  `Generated icon manifest with ${Object.keys(extMap).length} extensions, ${Object.keys(nameMap).length} filenames, ${Object.keys(folderMap).length} folders`,
);
